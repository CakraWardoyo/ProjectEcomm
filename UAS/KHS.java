    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
         DefaultTableModel tblKhs = (DefaultTableModel) tbl_mhs.getModel();
        tbl_mhs.setAutoCreateColumnsFromModel(true);
        List list = new ArrayList<>();
        int jmlBaris,sksQ,sksTotal=0;
        String hrf;
        double bbot,bbtTotal=0,bbtSks,totalIps;
        
        list.add(matkul.getSelectedItem().toString());
        list.add(sks.getText().toString());
        list.add(tugas.getText().toString());
        list.add(uts.getText().toString());
        list.add(uas.getText().toString());
        list.add(akhir.getText().toString());
        list.add(huruf.getText().toString());
        tblKhs.addRow(list.toArray());
        
        jmlBaris = tblKhs.getRowCount();
        
        for (int i = 0; i < jmlBaris; i++) {
            sksQ = Integer.valueOf(tblKhs.getValueAt(i, 1).toString());
            sksTotal = sksTotal + sksQ;
            
            hrf = tblKhs.getValueAt(i, 6).toString();
            if (hrf.equals("A")){
                bbot = 4.0;
            } else if (hrf.equals("AB")){
                bbot = 3.5;
            } else if (hrf.equals("B")){
                bbot = 3.0;
            } else if (hrf.equals("BC")){
                bbot = 2.5;
            } else if (hrf.equals("C")){
                bbot = 2.0;
            } else if (hrf.equals("D")){
                bbot = 1.0;
            } else {
                bbot = 0.0;
            }
            bbtSks = bbot * sksQ;
            bbtTotal = bbtTotal + bbtSks;
            
        }
        
        totalIps = bbtTotal / sksTotal;
        
        jml_sks.setText(String.valueOf(sksTotal));
        ips.setText(String.valueOf(totalIps));
        matkul.setSelectedIndex(0);
        sks.setText("");
        tugas.setText("");
        uts.setText("");
        uas.setText("");
        akhir.setText("");
        huruf.setText("");
        
        btnHapus.setEnabled(true);
    }//GEN-LAST:event_btnTambahActionPerformed

