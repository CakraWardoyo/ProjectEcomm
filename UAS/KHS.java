    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int selectedRow,opsi,jmlBaris;
        DefaultTableModel tblKhs = (DefaultTableModel) tbl_mhs.getModel();
        selectedRow = tbl_mhs.getSelectedRow();
        if (selectedRow >= 0) {
            opsi = JOptionPane.showConfirmDialog(null, "Apa benar akan dihapus?", "Peringatan", JOptionPane.YES_NO_OPTION);
            if (opsi == JOptionPane.YES_OPTION) {
                tblKhs.removeRow(selectedRow);
            }
        }
        jmlBaris = tblKhs.getRowCount();
        if (jmlBaris <= 0) {
            btnHapus.setEnabled(false);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

