    private void uasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uasActionPerformed
        // TODO add your handling code here:
        int nUAS,nTugas,nUTS,nilaiUasCheck;
        double nAkhir;
        String hruf;
        nilaiUasCheck = Integer.parseInt(uas.getText().toString());
        if (nilaiUasCheck > 100) {
            JOptionPane.showMessageDialog(null,"Nilai tidak boleh lebih dari 100","Peringatan",1);
        }else{
            if (tugas.getText().isEmpty()) {
                nTugas = 0;
            }else{
                nTugas = Integer.parseInt(tugas.getText().toString());
            }
            if (uts.getText().isEmpty()) {
                nUTS = 0;
            }else{
                nUTS = Integer.parseInt(uts.getText().toString());
            }
            if (uas.getText().isEmpty()) {
                nUAS = 0;
            }else{
                nUAS = Integer.parseInt(uas.getText().toString());
            }
            nAkhir = (0.2 * nTugas) + (0.3 * nUTS) + (0.5 * nUAS);
            akhir.setText(String.valueOf(nAkhir));
            if (nAkhir>=85){
                hruf = "A";
            } else if (nAkhir>=80){
                hruf = "AB";
            } else if (nAkhir>=70){
                hruf = "B";
            } else if (nAkhir>=65){
                hruf = "BC";
            } else if (nAkhir>=60){
                hruf = "C";
            } else if (nAkhir>=50){
                hruf = "D";
            } else {
                hruf = "E";
            }
            huruf.setText(hruf);
            btnTambah.setEnabled(true);
        }
    }//GEN-LAST:event_uasActionPerformed

