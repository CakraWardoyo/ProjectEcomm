    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int baris,jmlBaris;
        DefaultTableModel tableModel = (DefaultTableModel) tbl_mhs.getModel();
        baris = tbl_mhs.getSelectedRow();
        if (baris >= 0) {
            int opsi = JOptionPane.showConfirmDialog(null, "Apakah benar data ini akan Dihapus?", "Caution", JOptionPane.YES_NO_OPTION);
            if (opsi == JOptionPane.YES_OPTION) {
                tableModel.removeRow(baris);
            }
        }
        
        jmlBaris = tableModel.getRowCount();
        if (jmlBaris <= 0) {
            btnHapus.setEnabled(false);
            btnTambah.setEnabled(true);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

