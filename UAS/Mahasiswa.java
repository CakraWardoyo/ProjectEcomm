    void filterhuruf(KeyEvent a){
        char karakter = a.getKeyChar();
        if(!((karakter >= '0')&&(karakter <= '9')||(karakter == KeyEvent.VK_BACK_SPACE)||(karakter == KeyEvent.VK_DELETE) || (karakter == KeyEvent.VK_ENTER))){
            getToolkit().beep();
            a.consume();
            JOptionPane.showMessageDialog(null,"inputan hanya angka saja","Peringatan",1);
        }
    }

